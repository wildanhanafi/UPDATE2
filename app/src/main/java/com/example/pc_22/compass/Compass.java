    public void start() {
        if (mSensorManager.getDefaultSensor(Sensor.TYPE_ROTATION_VECTOR) == null) {
            if ((mSensorManager.getDefaultSensor(Sensor.TYPE_ACCELEROMETER) == null) || (mSensorManager.getDefaultSensor(Sensor.TYPE_MAGNETIC_FIELD) == null)) {
                noSensorsAlert();
            } else {
                mAccelerometer = mSensorManager.getDefaultSensor(Sensor.TYPE_ACCELEROMETER);
                mMagnetometer = mSensorManager.getDefaultSensor(Sensor.TYPE_MAGNETIC_FIELD);
                haveSensor = mSensorManager.registerListener(this, mAccelerometer, SensorManager.SENSOR_DELAY_UI);
                haveSensor2 = mSensorManager.registerListener(this, mMagnetometer, SensorManager.SENSOR_DELAY_UI);
            }
        } else {
            mRotationV = mSensorManager.getDefaultSensor(Sensor.TYPE_ROTATION_VECTOR);
            haveSensor = mSensorManager.registerListener(this, mRotationV, SensorManager.SENSOR_DELAY_UI);
        }

        dbRef.addValueEventListener(new ValueEventListener() {

            @Override
            public void onDataChange(DataSnapshot dataSnapshot) {
//                DataSnapshot compassSnapshot= dataSnapshot.child("compas-disiplin");
//                Iterable<DataSnapshot> compasChildren =compassSnapshot.getChildren();
                ArrayList < CompassModel > compassValue = new ArrayList<>();
                for (DataSnapshot compass : dataSnapshot.getChildren()) {
                    CompassModel c=compass.getValue(CompassModel.class);
//                    dbRef.child("compas-
                    c.setValue(mAzimuth);
                    dbRef.child("sensor").setValue(c);
//                    compassValue.add(c);
                    Toast.makeText(Compass.this, "Berhasil menambahkan data!", Toast.LENGTH_SHORT).show();
                }
            }

            @Override
            public void onCancelled(DatabaseError databaseError) {
                Toast.makeText(Compass.this, "Gagal menambahkan data!", Toast.LENGTH_SHORT).show();


            }
        });

    }

    public void noSensorsAlert() {
        AlertDialog.Builder alertDialog = new AlertDialog.Builder(this);
        alertDialog.setMessage("Your device doesn't support the Compass.")
                .setCancelable(false)
                .setNegativeButton("Close", new DialogInterface.OnClickListener() {
                    public void onClick(DialogInterface dialog, int id) {
                        finish();
                    }
                });
        alertDialog.show();
    }

    public void stop() {
        if (haveSensor) {
            mSensorManager.unregisterListener(this, mRotationV);
        } else {
            mSensorManager.unregisterListener(this, mAccelerometer);
            mSensorManager.unregisterListener(this, mMagnetometer);
        }
    }

    public void addData() {
        Map<String, Object> sensor = new HashMap<>();
        sensor.put("Value", txt_compass.getText().toString());

        myDB.collection("compass")
                .add(sensor)
                .addOnSuccessListener(new OnSuccessListener<DocumentReference>() {
                    @Override
                    public void onSuccess(DocumentReference documentReference) {
                        Toast.makeText(Compass.this, "Sukses Menambahkan data", Toast.LENGTH_SHORT).show();
                    }


                })


                .addOnFailureListener(new OnFailureListener() {
                    @Override
                    public void onFailure(@NonNull Exception e) {
                        Toast.makeText(Compass.this, "Gagal Menambahkan data", Toast.LENGTH_SHORT).show();

                    }
                });

    }

